package ua.com.merchik.merchik.data;

public class ListExchange {

    private int count;
    private String text;

    public ListExchange() {
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
